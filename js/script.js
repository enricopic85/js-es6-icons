const createCard=(data) =>{
    data.forEach(function(element) {
        container.innerHTML +=`<div  class="card">
        <i class="${element.family} ${element.prefix}${element.name} ${element.color} fs"></i>
        <p>${element.name.toUpperCase()}</p>
       </div>`
    })
}
let container=document.getElementById("container")
let select=document.getElementById("optionSelect");

createCard(cardIcon)

select.addEventListener('change',function(){
    container.innerHTML=""
    const filtering= cardIcon.filter(function(element){
        return select.value===element.type
    })
    createCard(filtering)
    if (select.value==="all") {
        createCard(cardIcon)
    }
})



